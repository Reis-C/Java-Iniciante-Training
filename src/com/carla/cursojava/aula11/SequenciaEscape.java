package com.carla.cursojava.aula11;

public class SequenciaEscape {

	public static void main(String[] args) {
		
		// "Hello, World!" 
		// \r retorno de carro - voltar para o inicio da linha
		// add \n\r é o mesmo que add o ln no print = println
		System.out.print("\"Hello, World!\"\n\r");
		// 1\4
		System.out.println("1\\4");

	}

}
